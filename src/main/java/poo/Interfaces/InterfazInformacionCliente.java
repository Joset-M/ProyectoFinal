    private void jBMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBMostrarActionPerformed
        String ciBuscado = jTCIBusqueda.getText().trim();
        boolean encontrado = false;
        

    try (BufferedReader br = new BufferedReader(new FileReader("registro/clientes.txt"))) {
        String linea;
        while ((linea = br.readLine()) != null) {
            if (linea.contains(ciBuscado)) {
                jTMostrarInfo.setText(linea);
                encontrado = true;
                break;
            }
        }

            if (!encontrado) {
                jTMostrarInfo.setText("Cliente no encontrado.");
            }
        } catch (IOException e) {
            jTMostrarInfo.setText("Error al leer el archivo: " + e.getMessage());
        }
    }//GEN-LAST:event_jBMostrarActionPerformed

